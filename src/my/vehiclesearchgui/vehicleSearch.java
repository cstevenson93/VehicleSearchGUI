    private void searchVehicleTagButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchVehicleTagButtonActionPerformed
        if(!tagField.getText().endsWith(" "))
        {

            int input = JOptionPane.showConfirmDialog(null, "Are these values correct?"
                + "\n\n\n"
                + "Tag: " + tagField.getText()+"\n");

            // 0=yes, 1=no, 2=cancel

            if (input == 0)
            {
                vehicle.searchByTag(tagField.getText());

            }

        }
        else
        {
            JOptionPane.showMessageDialog(null,
                "No vehicles were found belonging to that tag\n"
                + "Please make sure your input is correct and that there are no unnecessary spaces.",
                "Input Error",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_searchVehicleTagButtonActionPerformed

