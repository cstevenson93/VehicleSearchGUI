    private void searchVehicleNameButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchVehicleNameButtonActionPerformed

        if(nameField.getText().contains(" ") && !nameField.getText().endsWith(" "))
        {

            int input = JOptionPane.showConfirmDialog(null, "Are these values correct?"
                + "\n\n\n"
                + "Employee: " + nameField.getText()+"\n");

            // 0=yes, 1=no, 2=cancel

            if (input == 0)
            {
                vehicle.searchByFullName(nameField.getText());

            }

        }
        else
        {
            JOptionPane.showMessageDialog(null,
                "No vehicles were found belonging to that name\n"
                + "Please make sure your input is correct and that there are no unnecessary spaces.",
                "Input Error",
                JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_searchVehicleNameButtonActionPerformed

